package com.bdqn.test2;

/**
 * 级别类
 * @author huqiaohui
 *
 */
public class Level {
	private int levelNo;//各级别号
	private int strLength;//各级别输出的字符串的长度
	private int strTimes;//输出字符串的次数
	private int timeLimit;//各级别闯关的时间限制
	private int perScore;//在玩家成功输入字符串之后增加的分数值
	
	public Level() {
	}
	public Level(int levelNo, int strLength, int strTimes, int timeLimit, int perScore) {
		this.levelNo = levelNo;
		this.strLength = strLength;
		this.strTimes = strTimes;
		this.timeLimit = timeLimit;
		this.perScore = perScore;
	}
	public int getLevelNo() {
		return levelNo;
	}
	public void setLevelNo(int levelNo) {
		this.levelNo = levelNo;
	}
	public int getStrLength() {
		return strLength;
	}
	public void setStrLength(int strLength) {
		this.strLength = strLength;
	}
	public int getStrTimes() {
		return strTimes;
	}
	public void setStrTimes(int strTimes) {
		this.strTimes = strTimes;
	}
	public int getTimeLimit() {
		return timeLimit;
	}
	public void setTimeLimit(int timeLimit) {
		this.timeLimit = timeLimit;
	}
	public int getPerScore() {
		return perScore;
	}
	public void setPerScore(int perScore) {
		this.perScore = perScore;
	}
	
}
